package modelo;

/**
 *
 * @author aluno
 */
public class Professor {
    
    private String nome;
    private String siape;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSiape() {
        return siape;
    }

    public void setSiape(String siape) {
        this.siape = siape;
    }

    public Professor() {
        this.nome = null;
        this.siape = null;
    }
    
    
}
